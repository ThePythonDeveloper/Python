# Picks a random item from an list
import random

LIST = ["Dog", "Cat", "Fish", "Andrew Tate", "Mole", "Joe Biden", "Snake", "Watermelon"]

# we are now going to pick something random from the list using random.choice

choice = random.choice(LIST)
print(choice) # so it displays the output then shows it, for the first time I ran it, it was "Cat"